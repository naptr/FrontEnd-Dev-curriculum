// Perulangan

// For
for (var x = 1; x < 10; x += 1) {
  // aksi
  console.log("lagi ngeloop");
}

// While
var x = 0;
while (x < 10) {  // -> check then output
  console.log("masih ngeloop");
  x++;
}

// Do -> While
var x = 0;
do {
  console.log("lho, masih?") // -> output then check
  x++;
} while (x < 10);

